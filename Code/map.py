class GameMap:
    def __init__(self, game):
        self.game = game
        self.mini_map = mini_map
        self.world_map = {}
        self.get_map()

    def get_map(self):
        for i, row in enumerate(self.mini_map):
            for j, value in enumerate(row):
                if value:
                    self.world_map[(j, i)] = value

    def draw_map(self):
        for pos in self.world_map:
            pg.draw.rect(
                self.game.SCREEN,
                'darkgray',
                (pos[0] * TILE_X, pos[1] * TILE_Y, TILE_X, TILE_Y),
                2
            )
